            'ttl' => 'required',
            'alamat' => 'required',
            'jenkel' => 'required',
            'telp' => 'required',
            ]);

               BiodataModel::create([
            'nama' => $request->nama,
            'ttl' => $request->ttl,
            'alamat' => $request->alamat,
            'jenkel' => $request->jenkel,
            'telp' => $request->telp,
            ]);

            return back();

